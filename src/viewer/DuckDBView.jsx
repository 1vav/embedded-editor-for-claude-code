// TableView — full-tab view for .duckdb files.
// TableEmbed — inline embed for ![[name.duckdb]] in notes.
import React, { useState, useEffect, useRef, useCallback } from "react";
import { EditorView, keymap } from "@codemirror/view";
import { EditorState } from "@codemirror/state";
import { sql } from "@codemirror/lang-sql";
import { defaultKeymap, history, historyKeymap } from "@codemirror/commands";

const enc = encodeURIComponent;
const api = {
  getRows: (name, sqlStr) => {
    const url = sqlStr
      ? `/api/table/${enc(name)}?sql=${enc(sqlStr)}`
      : `/api/table/${enc(name)}`;
    return fetch(url).then(r => r.json());
  },
  upsertRows: (name, table, rows) =>
    fetch(`/api/table/${enc(name)}/rows`, {
      method: "PUT",
      headers: { "Content-Type": "application/json", "Origin": location.origin },
      body: JSON.stringify({ table, rows }),
    }).then(r => r.json()),
  runQuery: (name, sqlStr) =>
    fetch(`/api/table/${enc(name)}/query`, {
      method: "POST",
      headers: { "Content-Type": "application/json", "Origin": location.origin },
      body: JSON.stringify({ sql: sqlStr }),
    }).then(r => r.json()),
  getMeta: (name) =>
    fetch(`/api/table/${enc(name)}/query`, {
      method: "POST",
      headers: { "Content-Type": "application/json", "Origin": location.origin },
      body: JSON.stringify({ sql: "SELECT key, value FROM _ee_meta WHERE key IN ('created_by', 'updated_at')" }),
    }).then(r => r.json()),
  listUserTables: (name) =>
    fetch(`/api/table/${enc(name)}/query`, {
      method: "POST",
      headers: { "Content-Type": "application/json", "Origin": location.origin },
      body: JSON.stringify({ sql: "SELECT table_name FROM information_schema.tables WHERE table_schema='main' AND table_name NOT LIKE '_ee_%'" }),
    }).then(r => r.json()),
  createView: (name, view_name, sql) =>
    fetch(`/api/table/${enc(name)}/view`, {
      method: "POST",
      headers: { "Content-Type": "application/json", "Origin": location.origin },
      body: JSON.stringify({ view_name, sql }),
    }).then(r => r.json()),
  rename: (from, to, type) =>
    fetch("/api/rename", {
      method: "POST",
      headers: { "Content-Type": "application/json", "Origin": location.origin },
      body: JSON.stringify({ from, to, type }),
    }).then(r => r.json()),
};

// Auto-detect the best column to group by (status/state/type/category/priority).
// ── Relative timestamp ────────────────────────────────────────────────────────
function fmtRelative(isoStr) {
  const ms = Date.now() - new Date(isoStr).getTime();
  const s = Math.floor(ms / 1000);
  if (s < 60)  return "just now";
  const m = Math.floor(s / 60);
  if (m < 60)  return `${m}m ago`;
  const h = Math.floor(m / 60);
  if (h < 24)  return `${h}h ago`;
  const d = Math.floor(h / 24);
  if (d < 30)  return `${d}d ago`;
  return new Date(isoStr).toLocaleDateString();
}

// ── Editable filename ─────────────────────────────────────────────────────────
// Clicking the filename makes it editable; Enter/blur commits via POST /api/rename.
function EditableFilename({ name, type, T, onRename }) {
  const [editing, setEditing] = useState(false);
  const [value,   setValue]   = useState(name);
  const inputRef = useRef(null);

  useEffect(() => { setValue(name); }, [name]);

  function startEdit(e) {
    e.stopPropagation();
    setEditing(true);
    setTimeout(() => { inputRef.current?.select(); }, 0);
  }

  async function commit() {
    setEditing(false);
    const newName = value.trim().replace(/\.duckdb$/i, "");
    if (!newName || newName === name) { setValue(name); return; }
    setValue(newName);
    const res = await api.rename(name + ".duckdb", newName + ".duckdb", type).catch(() => null);
    if (res?.error) { setValue(name); return; }
    onRename?.(newName);
  }

  if (editing) {
    return (
      <input ref={inputRef} value={value} size={Math.max(10, value.length + 2)}
        onChange={e => setValue(e.target.value)}
        onBlur={commit}
        onKeyDown={e => { if (e.key === "Enter") { e.preventDefault(); commit(); } if (e.key === "Escape") { setEditing(false); setValue(name); } }}
        style={{
          fontFamily: T.mono, fontSize: 12, fontWeight: 600, color: T.duck,
          background: T.surface2, border: `1px solid ${T.duck}55`, borderRadius: 4,
          padding: "1px 6px", outline: "none",
        }}
      />
    );
  }
  return (
    <span onClick={startEdit} title="Click to rename"
      style={{ fontFamily: T.mono, fontSize: 12, fontWeight: 600, color: T.duck,
        cursor: "text", borderBottom: `1px dotted ${T.duck}44` }}>
      {name}.duckdb
    </span>
  );
}

// ── Wikilink cell renderer ────────────────────────────────────────────────────
// Renders a table cell value. If the value looks like a workspace file reference
// (ends in a known extension, or matches [[name]] syntax) it becomes a clickable
// navigation link. Columns ending in _name that were auto-generated by create_view
// are treated as the link label for the companion file column.
const WS_EXT_RE = /\.(excalidraw|md|tldraw|duckdb)$/i;
const WIKILINK_RE = /^\[\[(.+)\]\]$/;

function extToType(ext) {
  const e = (ext || "").toLowerCase().replace(".", "");
  if (e === "excalidraw") return "diagram";
  if (e === "tldraw")     return "tldraw";
  if (e === "duckdb")     return "table";
  return "note";
}

function CellValue({ value, T, onOpen }) {
  const str = String(value ?? "");

  // Explicit [[wikilink]] syntax (inserted by create_view or write_rows)
  const wlMatch = str.match(WIKILINK_RE);
  if (wlMatch) {
    const ref = wlMatch[1];
    const extMatch = ref.match(WS_EXT_RE);
    const type = extMatch ? extToType(extMatch[0]) : "note";
    const namePart = extMatch ? ref.slice(0, -extMatch[0].length) : ref;
    return (
      <span onClick={() => onOpen?.(namePart, type)}
        style={{ color: T.blue, cursor: "pointer", textDecoration: "underline", textDecorationStyle: "dotted" }}
        title={`Open ${ref}`}>
        {namePart}
      </span>
    );
  }

  // Raw file reference ending in a known workspace extension
  const extMatch = str.match(WS_EXT_RE);
  if (extMatch) {
    const namePart = str.slice(0, -extMatch[0].length).replace(/^\.?\//, "");
    const type = extToType(extMatch[0]);
    return (
      <span onClick={() => onOpen?.(namePart, type)}
        style={{ color: T.blue, cursor: "pointer", textDecoration: "underline", textDecorationStyle: "dotted" }}
        title={`Open ${str}`}>
        {str}
      </span>
    );
  }

  // Plain value
  return <>{str}</>;
}

function detectGroupCol(columns) {
  if (!columns?.length) return null;
  const exact = columns.find(c => /^(status|state|stage|type|category|priority|phase|group|label)$/i.test(c));
  if (exact) return exact;
  const loose = columns.find(c => /status|state|stage|type|categor|priorit/i.test(c));
  return loose ?? null;
}

export function TableView({ name, T, onOpen, onRename }) {
  const [dataView,    setDataView]    = useState(() => localStorage.getItem(`ee-table-view-${name}`) ?? "table");
  const [queryOpen,   setQueryOpen]   = useState(false);
  const [liveSync,    setLiveSync]    = useState(() => localStorage.getItem(`ee-table-sync-${name}`) === "live");
  const [isQuery,     setIsQuery]     = useState(false);
  const [updatedAt,   setUpdatedAt]   = useState(null);
  const [activeTable, setActiveTable] = useState(null);
  const [result,      setResult]      = useState({ columns: [], rows: [], rowCount: 0 });
  const [sqlText,     setSqlText]     = useState("");
  const [loading,     setLoading]     = useState(false);
  const [error,       setError]       = useState(null);

  // groupBy: undefined = not yet resolved (will auto-detect), null = explicit "None", string = column name
  const [groupBy, setGroupBy] = useState(() => {
    const saved = localStorage.getItem(`ee-table-group-${name}`);
    if (saved === null) return undefined;   // no saved pref → auto-detect on load
    return saved === "" ? null : saved;     // "" = saved "None", otherwise column name
  });
  const [groupPickerOpen, setGroupPickerOpen] = useState(false);
  const groupPickerRef = useRef(null);

  const [selectedOrigIndices, setSelectedOrigIndices] = useState(() => new Set());
  const lastClickedIndexRef = useRef(null);

  const cmRef     = useRef(null);
  const cmViewRef = useRef(null);

  // Auto-detect groupBy once columns are loaded (only if no saved preference).
  useEffect(() => {
    if (!result.columns.length) return;
    setGroupBy(prev => {
      if (prev !== undefined) return prev;        // already resolved
      return detectGroupCol(result.columns);      // first load: auto-detect
    });
  }, [result.columns]);

  // Close group picker on outside click.
  useEffect(() => {
    if (!groupPickerOpen) return;
    const handler = e => {
      if (groupPickerRef.current && !groupPickerRef.current.contains(e.target)) {
        setGroupPickerOpen(false);
      }
    };
    document.addEventListener("mousedown", handler);
    return () => document.removeEventListener("mousedown", handler);
  }, [groupPickerOpen]);

  function setGroupAndSave(col) {
    setGroupBy(col);
    localStorage.setItem(`ee-table-group-${name}`, col ?? "");
    setGroupPickerOpen(false);
  }

  function fetchTableRows(tbl) {
    if (!tbl) return;
    setLoading(true); setError(null);
    api.runQuery(name, `SELECT * FROM "${tbl.replace(/"/g, '""')}" LIMIT 200`)
      .then(res => { if (res.error) setError(res.error); else setResult(res); })
      .catch(e => setError(e.message))
      .finally(() => setLoading(false));
  }

  // Clear row selection on file/table navigation.
  useEffect(() => {
    setSelectedOrigIndices(new Set());
    lastClickedIndexRef.current = null;
  }, [name, activeTable]);

  // Load table list + meta on mount
  useEffect(() => {
    let cancelled = false;
    api.listUserTables(name).then(({ rows }) => {
      if (cancelled) return;
      const names = (rows || []).map(r => r.table_name);
      if (names.length) { setActiveTable(names[0]); fetchTableRows(names[0]); }
    }).catch(() => {});
    api.getMeta(name).then(({ rows }) => {
      if (cancelled) return;
      const meta = Object.fromEntries((rows ?? []).map(r => [r.key, r.value]));
      setIsQuery(meta.created_by === "query");
      if (meta.updated_at) setUpdatedAt(meta.updated_at);
    }).catch(() => {});
    return () => { cancelled = true; };
  }, [name]); // eslint-disable-line react-hooks/exhaustive-deps

  function fetchCustom(sqlStr) {
    const s = sqlStr ?? sqlText;
    if (!s) return;
    setLoading(true); setError(null);
    api.runQuery(name, s)
      .then(res => { if (res.error) setError(res.error); else setResult(res); })
      .catch(e => setError(e.message))
      .finally(() => setLoading(false));
  }

  async function handleCellBlur(rowIndex, col, newVal) {
    if (!activeTable) return;
    const row = { ...result.rows[rowIndex], [col]: newVal };
    setResult(r => { const rows = [...r.rows]; rows[rowIndex] = row; return { ...r, rows }; });
    await api.upsertRows(name, activeTable, [row]).catch(e => setError(e.message));
  }

  function handleAddRow() {
    if (!activeTable) return;
    const blank = Object.fromEntries(result.columns.map(c => [c, ""]));
    setResult(r => ({ ...r, rows: [...r.rows, blank] }));
  }

  function toggleDataView(v) {
    setDataView(v);
    localStorage.setItem(`ee-table-view-${name}`, v);
  }

  function toggleLive() {
    const next = !liveSync;
    setLiveSync(next);
    localStorage.setItem(`ee-table-sync-${name}`, next ? "live" : "lock");
  }

  // CM6 SQL editor in query pane
  useEffect(() => {
    if (!queryOpen || !cmRef.current || cmViewRef.current) return;
    const doc = sqlText || `SELECT * FROM ${activeTable ?? "table_name"} LIMIT 50`;
    const state = EditorState.create({
      doc,
      extensions: [
        history(), sql(),
        keymap.of([
          ...defaultKeymap, ...historyKeymap,
          { key: "Mod-Enter", run: () => { fetchCustom(cmViewRef.current?.state.doc.toString()); return true; } },
        ]),
        EditorView.updateListener.of(upd => { if (upd.docChanged) setSqlText(upd.state.doc.toString()); }),
        EditorView.theme({
          "&": { background: T.surface, color: T.text, fontSize: "12px" },
          ".cm-content": { fontFamily: T.mono, padding: "12px 14px" },
          ".cm-cursor": { borderLeftColor: T.accent },
        }),
      ],
    });
    cmViewRef.current = new EditorView({ state, parent: cmRef.current });
    return () => { cmViewRef.current?.destroy(); cmViewRef.current = null; };
  }, [queryOpen]);

  // Live re-run on SSE events when in query + live mode
  useEffect(() => {
    if (!isQuery || !liveSync) return;
    const es = new EventSource("/events");
    const handler = () => { if (sqlText) fetchCustom(sqlText); };
    es.addEventListener("note:changed", handler);
    es.addEventListener("table:changed", handler);
    return () => es.close();
  }, [isQuery, liveSync, sqlText]);

  // Push selected rows to server whenever selection changes.
  useEffect(() => {
    if (!selectedOrigIndices.size) {
      fetch("/api/selection", {
        method: "DELETE",
        headers: { Origin: location.origin },
      }).catch(() => {});
      return;
    }

    const schema = (result?.columns || []).map(col => ({
      column: col,
      type: result?.columnTypes?.[col] ?? "TEXT",
    }));

    const sortedIndices = [...selectedOrigIndices].sort((a, b) => a - b);
    const selectedRows = sortedIndices.map(origIndex => ({
      rowIndex: origIndex,
      data: result.rows[origIndex] ?? {},
    }));

    fetch("/api/selection", {
      method: "PUT",
      headers: { "Content-Type": "application/json", Origin: location.origin },
      body: JSON.stringify({
        type: "duckdb",
        file: name.endsWith(".duckdb") ? name : `${name}.duckdb`,
        tableName: activeTable || name,
        schema,
        selectedRows,
        totalRows: result.rows.length,
        currentQuery: isQuery ? (sqlText || null) : null,
      }),
    }).catch(() => {});
  }, [selectedOrigIndices, result, activeTable, name, sqlText]);

  // origIndex = position in the unsorted rows array; displayOrigIndices = sorted origIndex list for shift-range
  const handleRowClick = useCallback((origIndex, e, displayOrigIndices) => {
    setSelectedOrigIndices(prev => {
      const next = new Set(prev);
      if (e.metaKey || e.ctrlKey) {
        if (next.has(origIndex)) next.delete(origIndex);
        else next.add(origIndex);
      } else if (e.shiftKey && lastClickedIndexRef.current !== null && displayOrigIndices) {
        const anchorPos = displayOrigIndices.indexOf(lastClickedIndexRef.current);
        const clickPos  = displayOrigIndices.indexOf(origIndex);
        if (anchorPos !== -1 && clickPos !== -1) {
          const lo = Math.min(anchorPos, clickPos);
          const hi = Math.max(anchorPos, clickPos);
          for (let i = lo; i <= hi; i++) next.add(displayOrigIndices[i]);
        } else {
          next.add(origIndex);
        }
      } else {
        if (next.size === 1 && next.has(origIndex)) next.clear();
        else { next.clear(); next.add(origIndex); }
      }
      return next;
    });
    lastClickedIndexRef.current = origIndex;
  }, []);

  const ghost = (label, active, onClick, title) => (
    <span onClick={onClick} title={title} style={{
      padding: "3px 9px", border: `1px solid ${active ? T.duck + "66" : T.border2}`,
      borderRadius: 5, cursor: "pointer", fontSize: 11, fontFamily: T.mono,
      color: active ? T.duck : T.muted, background: active ? T.duck + "11" : "transparent",
      userSelect: "none",
    }}>{label}</span>
  );

  // Effective groupBy (treat undefined as null for rendering)
  const effectiveGroupBy = groupBy === undefined ? null : groupBy;

  return (
    <div style={{ flex: 1, display: "flex", flexDirection: "column", overflow: "hidden", background: T.bg }}>
      {/* Toolbar */}
      <div style={{ display: "flex", alignItems: "center", gap: 5, padding: "7px 12px",
        borderBottom: `1px solid ${T.border}`, background: T.surface, flexShrink: 0, flexWrap: "wrap" }}>
        <EditableFilename name={name} type="table" T={T} onRename={n => { onRename?.(n); onOpen?.(n, "table"); }} />
        <span style={{ fontFamily: T.mono, fontSize: 11, color: T.muted }}>· {result.rowCount ?? result.rows.length} rows</span>
        {updatedAt && (
          <span title={new Date(updatedAt).toLocaleString()} style={{ fontFamily: T.mono, fontSize: 10, color: T.muted, opacity: 0.6 }}>
            · updated {fmtRelative(updatedAt)}
          </span>
        )}
        <span style={{ color: T.border2, margin: "0 3px" }}>|</span>
        {ghost("⌘ Query", queryOpen, () => setQueryOpen(o => !o), "Toggle SQL query pane")}
        {ghost("≡ Table", dataView === "table", () => toggleDataView("table"), "Table view")}
        {ghost("⊞ Cards", dataView === "cards", () => toggleDataView("cards"), "Card view")}

        {/* Group picker — only in cards mode */}
        {dataView === "cards" && result.columns.length > 0 && (
          <>
            <span style={{ color: T.border2, margin: "0 3px" }}>|</span>
            <span ref={groupPickerRef} style={{ position: "relative" }}>
              <span onClick={() => setGroupPickerOpen(o => !o)} title="Group cards by column" style={{
                padding: "3px 9px", border: `1px solid ${effectiveGroupBy ? T.duck + "66" : T.border2}`,
                borderRadius: 5, cursor: "pointer", fontSize: 11, fontFamily: T.mono,
                color: effectiveGroupBy ? T.duck : T.muted,
                background: effectiveGroupBy ? T.duck + "11" : "transparent",
                userSelect: "none",
              }}>
                ⊟ {effectiveGroupBy ? `by ${effectiveGroupBy}` : "Group"} ▾
              </span>
              {groupPickerOpen && (
                <div style={{
                  position: "absolute", top: "calc(100% + 4px)", left: 0, zIndex: 200,
                  background: T.surface, border: `1px solid ${T.border}`, borderRadius: 6,
                  boxShadow: "0 4px 16px rgba(0,0,0,.3)", minWidth: 150, overflow: "hidden",
                }}>
                  <div style={{ padding: "5px 0" }}>
                    <div onClick={() => setGroupAndSave(null)}
                      style={{
                        padding: "6px 14px", fontFamily: T.mono, fontSize: 11, cursor: "pointer",
                        color: effectiveGroupBy === null ? T.duck : T.muted,
                        background: effectiveGroupBy === null ? T.duck + "11" : "transparent",
                      }}>
                      None
                    </div>
                    {result.columns.map(col => (
                      <div key={col} onClick={() => setGroupAndSave(col)}
                        style={{
                          padding: "6px 14px", fontFamily: T.mono, fontSize: 11, cursor: "pointer",
                          color: effectiveGroupBy === col ? T.duck : T.text,
                          background: effectiveGroupBy === col ? T.duck + "11" : "transparent",
                        }}>
                        {col}
                      </div>
                    ))}
                  </div>
                </div>
              )}
            </span>
          </>
        )}

        <span style={{ color: T.border2, margin: "0 3px" }}>|</span>
        {ghost("↓ Export", false, () => exportCsv(result, name), "Download as CSV")}
        <span style={{ flex: 1 }} />
        {isQuery && ghost(liveSync ? "⊙ Live" : "⊙ Lock", liveSync, toggleLive,
          liveSync ? "Re-runs on note changes. Click to lock." : "Locked. Click for live sync.")}
      </div>

      {/* Body */}
      <div style={{ flex: 1, display: "flex", overflow: "hidden" }}>
        {/* Query pane */}
        <div style={{
          width: queryOpen ? 300 : 0, overflow: "hidden", flexShrink: 0,
          borderRight: queryOpen ? `1px solid ${T.border}` : "none",
          background: T.surface, display: "flex", flexDirection: "column",
          transition: "width .2s ease",
        }}>
          <div style={{ padding: "7px 12px", fontSize: 10, color: T.muted, letterSpacing: ".07em",
            fontFamily: T.mono, borderBottom: `1px solid ${T.border}`, whiteSpace: "nowrap" }}>
            SQL · QUERY
          </div>
          <div ref={cmRef} style={{ flex: 1, overflow: "auto" }} />
          <div style={{ padding: "8px 12px" }}>
            <span onClick={() => fetchCustom(cmViewRef.current?.state.doc.toString())} style={{
              display: "inline-block", padding: "4px 12px", background: T.surface2,
              border: `1px solid ${T.duck}44`, color: T.duck, borderRadius: 5,
              fontSize: 11, fontFamily: T.mono, cursor: "pointer",
            }}>▶ Run <span style={{ color: T.muted, marginLeft: 4 }}>⌘↵</span></span>
          </div>
        </div>

        {/* Data pane */}
        <div style={{ flex: 1, overflow: "auto" }}>
          {error && <div style={{ padding: "10px 14px", color: T.red || "#f87171", fontFamily: T.mono, fontSize: 12 }}>{error}</div>}
          {loading && <div style={{ padding: "10px 14px", color: T.muted, fontFamily: T.mono, fontSize: 12 }}>Loading…</div>}
          {!loading && dataView === "table" && (
            <TableDataView result={result} T={T} onCellBlur={handleCellBlur} onAddRow={handleAddRow} onOpen={onOpen}
              selectedOrigIndices={selectedOrigIndices} onRowClick={handleRowClick} />
          )}
          {!loading && dataView === "cards" && (
            <CardDataView result={result} T={T} onCellBlur={handleCellBlur} onAddRow={handleAddRow} groupBy={effectiveGroupBy} />
          )}
        </div>
      </div>
    </div>
  );
}

function exportCsv({ columns, rows }, name) {
  if (!columns?.length) return;
  const header = columns.join(",");
  const body   = rows.map(r => columns.map(c => JSON.stringify(r[c] ?? "")).join(",")).join("\n");
  const blob   = new Blob([header + "\n" + body], { type: "text/csv" });
  const a = document.createElement("a"); a.href = URL.createObjectURL(blob);
  a.download = `${name || "export"}.csv`; a.click();
}

// ── Shared sort comparator ────────────────────────────────────────────────────
// Numeric-aware, locale-sensitive. Used by TableDataView and TableEmbed.
function sortRows(rows, col, dir) {
  return [...rows].sort((a, b) => {
    const av = String(a[col] ?? ""), bv = String(b[col] ?? "");
    const numA = parseFloat(av), numB = parseFloat(bv);
    const cmp = (!isNaN(numA) && !isNaN(numB))
      ? numA - numB
      : av.localeCompare(bv, undefined, { numeric: true, sensitivity: "base" });
    return dir === "asc" ? cmp : -cmp;
  });
}

function TableDataView({ result, T, onCellBlur, onAddRow, onOpen, selectedOrigIndices, onRowClick }) {
  const { columns, rows } = result;
  const [sortCol, setSortCol] = useState(null);
  const [sortDir, setSortDir] = useState("asc"); // "asc" | "desc"

  function handleSort(col) {
    if (sortCol === col) {
      setSortDir(d => d === "asc" ? "desc" : "asc");
    } else {
      setSortCol(col);
      setSortDir("asc");
    }
  }

  // Sort {row, origIndex} pairs so that onCellBlur always references the correct
  // original row index regardless of current sort order.
  const indexedRows = rows.map((row, origIndex) => ({ row, origIndex }));
  if (sortCol) {
    indexedRows.sort(({ row: a }, { row: b }) => {
      const av = String(a[sortCol] ?? ""), bv = String(b[sortCol] ?? "");
      const numA = parseFloat(av), numB = parseFloat(bv);
      const cmp = (!isNaN(numA) && !isNaN(numB)) ? numA - numB
        : av.localeCompare(bv, undefined, { numeric: true, sensitivity: "base" });
      return sortDir === "asc" ? cmp : -cmp;
    });
  }

  if (!columns?.length) return (
    <div style={{ padding: 20, color: T.muted, fontFamily: T.mono, fontSize: 12 }}>
      No data. Run a query or add rows.
    </div>
  );
  return (
    <table style={{ width: "100%", borderCollapse: "collapse", fontSize: 12, fontFamily: T.mono }}>
      <thead>
        <tr>
          {columns.map(c => (
            <th key={c} onClick={() => handleSort(c)}
              style={{ padding: "7px 12px", textAlign: "left", fontSize: 10, fontWeight: 500,
                color: sortCol === c ? T.duck : T.muted, letterSpacing: ".06em", textTransform: "uppercase",
                borderBottom: `1px solid ${T.border}`, background: T.surface, whiteSpace: "nowrap",
                cursor: "pointer", userSelect: "none" }}>
              {c}{sortCol === c ? (sortDir === "asc" ? " ▲" : " ▼") : ""}
            </th>
          ))}
        </tr>
      </thead>
      <tbody>
        {indexedRows.map(({ row, origIndex }) => (
          <tr key={origIndex}
            onClick={e => onRowClick?.(origIndex, e, indexedRows.map(r => r.origIndex))}
            style={{
              borderBottom: `1px solid ${T.border}`,
              cursor: "pointer",
              background: selectedOrigIndices?.has(origIndex) ? T.accent + "22" : undefined,
              userSelect: "none",
            }}>
            {columns.map(c => {
              const val = String(row[c] ?? "");
              const isLink = WIKILINK_RE.test(val) || WS_EXT_RE.test(val);
              return isLink ? (
                <td key={c} style={{ padding: "7px 12px", whiteSpace: "nowrap" }}>
                  <CellValue value={val} T={T} onOpen={onOpen} />
                </td>
              ) : (
                <td key={c} contentEditable suppressContentEditableWarning
                  onBlur={e => onCellBlur(origIndex, c, e.currentTarget.textContent)}
                  onFocus={e => e.currentTarget.style.background = T.surface2}
                  onBlurCapture={e => e.currentTarget.style.background = "transparent"}
                  style={{ padding: "7px 12px", color: T.textDim, outline: "none", whiteSpace: "nowrap" }}>
                  {val}
                </td>
              );
            })}
          </tr>
        ))}
      </tbody>
      <tfoot>
        <tr>
          <td colSpan={columns.length}>
            <button onClick={onAddRow} style={{
              padding: "8px 12px", color: T.muted, fontSize: 11, fontFamily: T.mono,
              cursor: "pointer", textAlign: "left", width: "100%", background: "transparent",
              border: "none", borderTop: `1px solid ${T.border}`,
            }}
            onMouseEnter={e => e.currentTarget.style.color = T.duck}
            onMouseLeave={e => e.currentTarget.style.color = T.muted}>
              + Add row
            </button>
          </td>
        </tr>
      </tfoot>
    </table>
  );
}

// Sort group keys: semantic order for status-like columns, otherwise alphabetical.
function sortGroupKeys(keys, groupByCol) {
  const isStatusLike = /status|state|stage|phase/i.test(groupByCol ?? "");
  if (!isStatusLike) return [...keys].sort((a, b) => a.localeCompare(b));
  const rank = v => {
    const lo = v.toLowerCase();
    if (/done|complet|accept|shipped|closed/.test(lo)) return 5;
    if (/progress|review|interview|active/.test(lo))   return 4;
    if (/applied|pending|wait|open|backlog/.test(lo))  return 3;
    if (/block|hold|paused/.test(lo))                  return 2;
    if (/reject|declin|cancel|abandon/.test(lo))       return 1;
    return 0;
  };
  // Within same rank, sort alphabetically.
  return [...keys].sort((a, b) => {
    const dr = rank(b) - rank(a);
    return dr !== 0 ? dr : a.localeCompare(b);
  });
}

const CARD_GRID = { display: "grid", gridTemplateColumns: "repeat(auto-fill, minmax(180px,1fr))", gap: 10 };

